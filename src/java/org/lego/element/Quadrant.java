package org.lego.element;

public class Quadrant {

    private int seq;
    private int x;
    private int y;
    /**
     * @param x the x to set
     */
    public void setX(int x) {
        this.x = x;
    }
    /**
     * @return the x
     */
    public int getX() {
        return x;
    }
    /**
     * @param y the y to set
     */
    public void setY(int y) {
        this.y = y;
    }
    /**
     * @return the y
     */
    public int getY() {
        return y;
    }
    /**
     * @param seq the seq to set
     */
    public void setSeq(int seq) {
        this.seq = seq;
    }
    /**
     * @return the seq
     */
    public int getSeq() {
        return seq;
    }
}
